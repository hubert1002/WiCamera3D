    private float[] scurve3d_h= new float[scurve2d.length];
    private float[] scurve3d_v= new float[scurve2d.length];
    
    private FloatBuffer m_vertex_buff_h = null;
    private FloatBuffer m_vertex_buff_v = null;
    
    public  SCurve()
    {
    	float k = 4.5f / 800; // 2D坐标下800宽度对应3D坐标下4.5宽度
    	for (int i = 0; i < scurve2d.length; i+=3)
    	{
    		scurve3d_h[i] = (scurve2d[i]) * k + x_offset_date_h;
    		scurve3d_h[i+1] = 0 + y_offset_date_h;
    		scurve3d_h[i+2] = -(scurve2d[i+1]) * k + z_offset_date_h;
    		
    		scurve3d_v[i] = (scurve2d[i]) * k + x_offset_date_v;
    		scurve3d_v[i+1] = 0 + y_offset_date_v;
    		scurve3d_v[i+2] = -(scurve2d[i+1]) * k + z_offset_date_v;
    	}
    	
    	 m_vertex_buff_h = ResourceManager.makeFloatBuffer(scurve3d_h);
    	 m_vertex_buff_v = ResourceManager.makeFloatBuffer(scurve3d_v);
    }
    
    public float[] getPosition(int index)
    {
    	float[] pos = {-10,0,0};
    	
    	if (index > scurve3d_h.length/3 - 3 || index < 0)
    		return pos;
    	
    	//0.28f, 0.1f, -0.01f
    	if (WiGalleryOpenGLRenderer.m_b_horizontal_screen)
    	{
	    	pos[0] = scurve3d_h[3*index]-0.3f + 0.28f;
	    	pos[1] = scurve3d_h[3*index+1] + 0.1f;
	    	pos[2] = scurve3d_h[3*index+2] - 0.01f;
    	}
    	else
    	{
	    	pos[0] = scurve3d_v[3*index]-0.3f + 0.28f;
	    	pos[1] = scurve3d_v[3*index+1] + 0.1f;
	    	pos[2] = scurve3d_v[3*index+2] - 0.01f;
    	}
    	
    	return pos;
    }
    
    public int getPositionIndex(int i)
    {   	
    	return (i+3)*m_step;
    }
    
    public void onDraw(GL10 gl)
    {
    	gl.glPushMatrix();
    	gl.glLineWidth(5.0f);
    	gl.glDisableClientState(GL10.GL_TEXTURE_COORD_ARRAY);
    	gl.glDisable(GL10.GL_TEXTURE_2D);
    	if (WiGalleryOpenGLRenderer.m_b_horizontal_screen)
    		gl.glVertexPointer(3, GL_FLOAT, 0, m_vertex_buff_h);
    	else
    		gl.glVertexPointer(3, GL_FLOAT, 0, m_vertex_buff_v);
        
       	gl.glDrawArrays(GL10.GL_LINE_STRIP, 0, scurve3d_v.length/3);
        gl.glEnable(GL10.GL_TEXTURE_2D);
        gl.glEnableClientState(GL10.GL_TEXTURE_COORD_ARRAY);
        gl.glPopMatrix();
    }
